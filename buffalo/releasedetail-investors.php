      </li>

      <li style="margin-bottom: 10.0px;">

        Traditional chicken wing inflation of 3.5% to 4.5%

      </li>

      <li style="margin-bottom: 10.0px;">

        Depreciation and amortization expense of <money>$153</money> to <money>$157 million</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        General and administrative expense of <money>$149</money> to <money>$153 million</money>, including

        stock-based compensation of <money>$12</money> to <money>$13 million</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        Operating income growth of 9% to 13% over 2016, including the 53rd week

