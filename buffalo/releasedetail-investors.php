      <li style="margin-bottom: 10.0px;">

        15 franchised <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings</org> locations in <location value="LC/us" idsrc="xmltag.org">the United States</location>

