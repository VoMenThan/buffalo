    <h1>Buffalo Wild Wings, Inc. Announces Fourth Quarter Earnings per Share of $0.87</h1>
      
    
    
      <div id="ndq-releasebody"><p>

<p style="text-align: center;">

      <b>- 2017 Earnings Per Diluted Share Outlook of <money>$5.60</money> to <money>$6.00</money> -</b>

    </p>







    <p>    <location value="LU/us.mn.minnea" idsrc="xmltag.org">MINNEAPOLIS</location>--(BUSINESS WIRE)--

      <a href="http://cts.businesswire.com/ct/CT?id=smartlink&url=http%3A%2F%2Fwww.BuffaloWildWings.com&esheet=51506605&newsitemid=20170207006431&lan=en-US&anchor=Buffalo+Wild+Wings%2C+Inc.&index=1&md5=1e503047423fc19bb4dd2d8038e7f931" rel="nofollow"><span style="text-decoration: underline;"><org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo

      Wild Wings, Inc.</org></span></a> (NASDAQ: BWLD) announced today financial

      results for the fourth quarter and fiscal year ended <chron>December 25, 2016</chron>.

    </p>

    <p>

      Key metrics for the fourth quarter, versus the same period a year ago,

      were:

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Total revenue increased 0.8% to <money>$494.2 million</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        Company<b>-</b>owned restaurant sales increased 0.9% to <money>$470.5 million</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        Same<b>-</b>store sales decreased 4.0% at company<b>-</b>owned

        restaurants and 3.9% at franchised restaurants

      </li>

      <li style="margin-bottom: 10.0px;">

        Net earnings decreased 38.2% to <money>$15.6 million</money> from <money>$25.3 million</money>, and

        earnings per diluted share decreased 34.1% to <money>$0.87</money> from <money>$1.32</money>

      </li>

    </ul>

    <p>

      Key metrics for the full year 2016, versus 2015, were:

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Total revenue increased 9.6% to <money>$2.0 billion</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        Company<b>-</b>owned restaurant sales increased 10.3% to <money>$1.9 billion</money>

      </li>

      <li style="margin-bottom: 10.0px;">

        Same<b>-</b>store sales decreased 2.4% at company<b>-</b>owned

        restaurants and 2.7% at franchised restaurants

      </li>

      <li style="margin-bottom: 10.0px;">

        Net earnings decreased 0.3% to <money>$94.7 million</money> from <money>$95.1 million</money>, and

        earnings per diluted share increased 3.0% to <money>$5.12</money> from <money>$4.97</money>

      </li>

    </ul>

    <p>

      <person value="APIN:3520027126">Sally Smith</person>, President and Chief Executive Officer, commented, "The

      challenging restaurant environment continued in the fourth quarter and

      culminated with a difficult December. Our key programs to drive traffic

      at <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings</org>, including FastBreak&#8482; Lunch, Half-Price Wing

      Tuesdays<sup>&#174;</sup>, and our Blazin' Rewards<sup>&#174;</sup> loyalty

      program, have driven positive traffic so far in the first quarter."

    </p>

    <p>

      <person>Ms. Smith</person> concluded, "Our focus for the year is to gain momentum on

      sales, improve our cost structure, grow internationally, optimize our

      domestic restaurant portfolio, and lower our cost of capital. The

      Buffalo Wild Wings brand remains differentiated, healthy, and strong and

      with operating income growing throughout the year. In 2017, we

      anticipate earnings per diluted share in the range of <money>$5.60</money> to <money>$6.00</money>. We

      also recently announced an expanded share repurchase program with an

      accelerated timeline to achieve our target of 1.5x net debt to EBITDA by

      year-end."

    </p>

    <p>

      <b>Revenue</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Total revenue increased 0.8% to <money>$494.2 million</money> in the fourth quarter,

        compared to <money>$490.2 million</money> in the fourth quarter of 2015. Full year

        total revenue increased 9.6% to <money>$2.0 billion</money> compared to <money>$1.8 billion</money>

        last year.

      </li>

      <li style="margin-bottom: 10.0px;">

        Company<b>-</b>owned restaurant sales for the fourth quarter increased

        0.9% over the same period in 2015, to <money>$470.5 million</money>.

      </li>

      <li style="margin-bottom: 10.0px;">

        Franchise royalties and fees decreased 0.4% on lower revenues to <money>$23.7

        million</money> for the quarter, versus <money>$23.8 million</money> in the fourth quarter of

        2015.

      </li>

      <li style="margin-bottom: 10.0px;">

        Average weekly sales for company-owned <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings</org> restaurants

        were <money>$59,120</money> for the fourth quarter of 2016, compared to <money>$61,971</money> for

        the same quarter last year, a 4.6% decrease due to same-store sales

        declines and more smaller-store openings.

      </li>

      <li style="margin-bottom: 10.0px;">

        Franchised <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings</org> locations in <location value="LC/us" idsrc="xmltag.org">the United States</location> averaged

        <money>$61,397</money> for the period versus <money>$64,032</money> in the fourth quarter a year

        ago, a 4.1% decrease.

      </li>

    </ul>

    <p>

      <b>Restaurant-level costs and expenses</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Cost of sales for the fourth quarter was 31.1% of restaurant sales

        compared to 29.5% in the fourth quarter last year, driven by higher

        traditional wing costs.



        <ul>

          <li style="margin-bottom: 10.0px;">

            Traditional wings were <money>$1.99</money> per pound in the fourth quarter,

            <money>$0.18</money>, 9.9%, higher than last year's fourth quarter average of

            <money>$1.81</money>.

          </li>

        </ul>

      </li>

      <li style="margin-bottom: 10.0px;">

        Cost of labor for the fourth quarter was 31.8% of restaurant sales, 90

        basis points higher than fourth quarter last year, resulting from

        sales deleverage, partially offset by lower Guest Experience Captain

        hours.

      </li>

      <li style="margin-bottom: 10.0px;">

        Restaurant operating expenses as a percentage of restaurant sales were

        15.7%, an increase of 30 basis points from the fourth quarter of 2015,

        primarily driven by higher insurance and repair and maintenance

        expenses.

      </li>

      <li style="margin-bottom: 10.0px;">

        Occupancy costs were 5.9% as a percentage of restaurant sales, 30

        basis points higher compared to the same quarter last year.

      </li>

      <li style="margin-bottom: 10.0px;">

        Restaurant-level profit was <money>$73.2 million</money>, or 15.6%, of restaurant

        sales, compared to <money>$86.7 million</money>, or 18.6%, in the fourth quarter last

        year. Full year restaurant level profit was <money>$333.9 million</money>, or 17.7%,

        90 basis points lower than the prior year's <money>$319.0 million</money> or 18.6%

        due largely to sales deleverage and higher cost of goods and labor.

      </li>

    </ul>

    <p>

      <b>Other Expenses</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Depreciation and amortization expense for the fourth quarter was <money>$38.3

        million</money>, increasing 4.6%, due to new unit openings.

      </li>

      <li style="margin-bottom: 10.0px;">

        General and administrative expenses were <money>$29.4 million</money> in the fourth

        quarter, decreasing 5.9% from the same period last year, due to lower

        compensation expense.



        <ul>

          <li style="margin-bottom: 10.0px;">

            Stock-based compensation was <money>($2.1) million</money> in the fourth quarter

            compared to <money>$2.0 million</money> of expense in the prior year period.

          </li>

        </ul>

      </li>

      <li style="margin-bottom: 10.0px;">

        Preopening expenses for the quarter totaled <money>$3.5 million</money>, versus <money>$4.9

        million</money> in the fourth quarter last year, due to smaller openings as

        well as reduced lease expense.

      </li>

      <li style="margin-bottom: 10.0px;">

        Loss on asset disposal for the fourth quarter totaled <money>$3.9 million</money>

        compared to last year of <money>$3.3 million</money>.

      </li>

      <li style="margin-bottom: 10.0px;">

        Interest expense was <money>$1.6 million</money> in the fourth quarter, compared to

        <money>$0.8 million</money> in the prior year period. The increase is a result of

        higher borrowing on the line of credit.

      </li>

      <li style="margin-bottom: 10.0px;">

        Other expense (income) was <money>$(1.3) million</money> for the quarter compared to

        <money>$73,000</money> of expense in 2015, due to the recognition of a gain from

        contingent consideration.

      </li>

      <li style="margin-bottom: 10.0px;">

        The effective tax rate during the quarter was 27.9%, compared to 24.7%

        in the prior year.

      </li>

    </ul>

    <p>

      <b>Earnings</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Operating income was <money>$21.8 million</money> in the fourth quarter, or 4.4% of

        total revenue, compared to <money>$34.5 million</money> and 7.0% in the prior year.

        For the year, operating income was <money>$136.7 million</money>, or 6.9% of total

        revenue, compared to <money>$138.5 million</money> and 7.6%.

      </li>

      <li style="margin-bottom: 10.0px;">

        Net earnings decreased 38.2% to <money>$15.6 million</money> in the fourth quarter,

        versus <money>$25.3 million</money> in the fourth quarter of 2015. For the full year,

        net earnings decreased 0.3% to <money>$94.7 million</money>, versus <money>$95.1 million</money> in

        2015.

      </li>

      <li style="margin-bottom: 10.0px;">

        Earnings per diluted share were <money>$0.87</money>, compared to fourth quarter 2015

        earnings per diluted share of <money>$1.32</money>. Earnings per diluted share

        increased 3.0% to <money>$5.12</money> in 2016, compared to <money>$4.97</money> in 2015, driven by

        the <money>$232.7 million</money> of share repurchases in fiscal 2016.

      </li>

    </ul>

    <p>

      <b>Balance Sheet</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Cash totaled <money>$49.3 million</money> at the end of 2016.

      </li>

      <li style="margin-bottom: 10.0px;">

        The credit facility had an outstanding balance of <money>$170.0 million</money> as of

        the end of the year.

      </li>

      <li style="margin-bottom: 10.0px;">

        The year ended with over <money>$1.0 billion</money> in total assets and <money>$517.9

        million</money> in total equity.

      </li>

    </ul>

    <p>

      <b>Cash Flow</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        Cash flow from operations was <money>$68.7 million</money> for the quarter, a 2.5%

        decrease over the fourth quarter last year. For the full year, cash

        flow from operations was <money>$282.6 million</money>, a 19.1% increase over 2015.

      </li>

      <li style="margin-bottom: 10.0px;">

        Free cash flow in the fourth quarter was <money>$44.8 million</money>, compared to

        <money>$22.1 million</money> in the prior year. Free cash flow in 2016 was <money>$140.9

        million</money>, compared to <money>$64.7 million</money> in the prior year.

      </li>

      <li style="margin-bottom: 10.0px;">

        827,639 shares were repurchased for a total of <money>$126.9 million</money> during

        the fourth quarter of 2016. For the year, 1,586,533 shares were

        repurchased for a total of <money>$232.7 million</money>.

      </li>

    </ul>

    <p>

      <b>2017 Outlook</b>

    </p>

    <p>

      <b>The company expects the following approximate new unit development in

      2017:</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

        15 company-owned <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings</org> restaurants in <location value="LC/us" idsrc="xmltag.org">the United States</location>

