      <b>About the Company</b>

    </p>

    <p>

      <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild Wings, Inc.</org>, founded in 1982 and headquartered in

      <location value="LU/us.mn.minnea" idsrc="xmltag.org">Minneapolis</location>, is a growing owner, operator and franchisor of <org value="NASDAQ-NMS:BWLD" idsrc="xmltag.org">Buffalo Wild

