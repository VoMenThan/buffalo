      <b>Restaurant-Level Profit and Restaurant-Level Margin</b>

    </p>

    <p>

      Restaurant-level profit and restaurant-level margin are neither required

      by, nor presented in accordance with <location value="LC/us" idsrc="xmltag.org">U.S.</location> GAAP and are non-GAAP

      financial measures. Restaurant-level profit is defined restaurant sales

      less restaurant operating costs (cost of sales, labor, operating, and

      occupancy expense). Restaurant-level margin is defined as

      restaurant-level profit as a percentage of restaurant sales.

      Restaurant-level profit and restaurant-level margin have limitations as

      analytical tools, and should not be evaluated in isolation or as

      substitutes for analysis of results as reported under <location value="LC/us" idsrc="xmltag.org">U.S.</location> GAAP.

      Management believes the restaurant-level profit and restaurant-level

      margin are important tools for investors because they are widely-used

      metrics within the restaurant industry to evaluate restaurant-level

      productivity, efficiency and performance. Management uses

      restaurant-level profit and restaurant-level margin as key performance

      indicators to evaluate the profitability of company-owned restaurants.

