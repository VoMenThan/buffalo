      <li style="margin-bottom: 10.0px;">

        20 franchised Buffalo Wild Wing locations internationally

      </li>

      <li style="margin-bottom: 10.0px;">

        2 company-owned and 12 to 15 franchised R Taco restaurants

      </li>

    </ul>

    <p>

      <b>The company expects the following in 2017:</b>

    </p>

    <ul>

      <li style="margin-bottom: 10.0px;">

